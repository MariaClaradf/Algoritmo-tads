# (Calculadora de Área de Retângulo) Receba a base e a altura de um retângulo e exiba a área dele usando a fórmula:
# area = base ∗ altura/2

print('Calculadora de Área de Retangulo')

base = float(input('Digite a base do seu retângulo: '))

alt = float(input('Digite a altura do seu retângulo: '))

area = base * alt / 2
print(f'A área calculada do seu retângulo é: {area}')